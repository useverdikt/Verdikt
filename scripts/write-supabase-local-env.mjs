#!/usr/bin/env node
/**
 * Writes frontend/.env.local from `npx supabase status -o json` (local stack).
 * Requires Docker + `supabase start` (or `supabase status` showing API URL).
 */
import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const run = (cmd) =>
  execSync(cmd, { encoding: "utf8", cwd: root, stdio: ["ignore", "pipe", "pipe"] });

function pick(obj, keys) {
  for (const k of keys) {
    if (obj[k] != null && String(obj[k]).length) return obj[k];
  }
  return undefined;
}

let json;
try {
  json = JSON.parse(run("npx supabase status -o json"));
} catch {
  console.error(
    "Could not run `npx supabase status -o json`. Start Docker, then:\n  npx supabase start"
  );
  process.exit(1);
}

const url = pick(json, ["API_URL", "api_url", "APIURL"]);
const anon = pick(json, ["anon_key", "ANON_KEY", "publishable_key"]);

if (!url || !anon) {
  console.error("Could not find API URL / anon key in status JSON. Keys:", Object.keys(json));
  process.exit(1);
}

const envPath = join(root, "frontend", ".env.local");
const lines = [
  "# Generated by scripts/write-supabase-local-env.mjs — gitignored",
  `VITE_SUPABASE_URL=${url}`,
  `VITE_SUPABASE_ANON_KEY=${anon}`,
  "",
];
writeFileSync(envPath, lines.join("\n"), "utf8");
console.log("Wrote", envPath);
